namespace PnP.Core.Model.SharePoint
{
    /// <summary>
    /// Enumeration of the different statuses of an alert
    /// </summary>
    public enum AlertStatus
    {
        /// <summary>
        /// The alert is on
        /// </summary>
        On = 0,

        /// <summary>
        /// The alert is off
        /// </summary>
        Off = 1,

        /// <summary>
        /// The alert is in error
        /// </summary>
        Error = 2,

    }
}
